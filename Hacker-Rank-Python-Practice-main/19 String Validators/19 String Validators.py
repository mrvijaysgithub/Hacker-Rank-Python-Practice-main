s = input()
for ele in s:
    if ele.isalnum():
        print (True)
        break
else:
    print(False)
    
    
for ele in s:
    if ele.isalpha():
        print (True)
        break
else:
    print(False)
    
for ele in s:
    if ele.isdigit():
        print (True)
        break
else:
    print(False)
    
    
for ele in s:
    if ele.islower():
        print (True)
        break
else:
    print(False)
    
    
for ele in s:
    if ele.isupper():
        print (True)
        break
else:
    print(False)